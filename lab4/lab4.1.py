class ArrayStack:
    def __init__(self):
        self.data = []
    def size(self):
        return len(self.data)
    def is_empty(self):
        if len(self.data)==0:
            return True
        else:
            return False
    def push(self, getdt):
        self.data.append(getdt)
        return
    def pop(self):
        if self.is_empty():
            return "Underflow: Cannot pop data from an empty list"
        else:
            return self.data.pop()

def matching(exp):
    stack = ArrayStack()
    for i in exp:
        if i == "(":
            stack.push(i)
        elif i == ")":
            if stack.is_empty():
                return False
            else:
                stack.pop()
    if stack.is_empty():
        return True
    else:
        return False

x = matching("((((A+B)*C))))")
print(x)
